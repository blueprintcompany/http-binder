using Microsoft.CodeAnalysis;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace HttpBinder.Generator
{
    /// <summary>
    /// Responsible for emitting the C# source code of the binder for a given
    /// bound type. The generated source follows idiomatic C# style and
    /// introduces helper methods as necessary to perform recursive binding.
    /// </summary>
    internal static class CodeRenderer
    {
        /// <summary>
        /// Emits the binder code for the provided <see cref="BoundType"/>.
        /// </summary>
        /// <param name="model">The bound model for which to emit code.</param>
        /// <returns>The generated C# source as a string.</returns>
        public static string Render(BoundType model)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("#nullable enable");
            sb.AppendLine();
            var typeSymbol = model.TypeSymbol;
            var ns = typeSymbol.ContainingNamespace.ToDisplayString();
            if (!string.IsNullOrEmpty(ns) && ns != "<global namespace>")
            {
                sb.Append("namespace ").AppendLine(ns).AppendLine("{");
            }
            var indentLevel = (!string.IsNullOrEmpty(ns) && ns != "<global namespace>") ? 1 : 0;
            var indent = new IndentedStringBuilder(sb, indentLevel);
            var typeKindKeyword = typeSymbol.IsRecord
                ? "record"
                : "class"
            indent.AppendLine($"partial {typeKindKeyword} {typeSymbol.Name}");
            indent.AppendLine("{");
            indent.Indent();
            // Render the main BindAsync method
            RenderBindMethod(indent, model);
            // Render any helper methods required for nested complex types
            RenderComplexHelpers(indent, model);
            indent.Unindent();
            indent.AppendLine("}");
            if (!string.IsNullOrEmpty(ns) && ns != "<global namespace>")
            {
                sb.AppendLine("}");
            }
            return sb.ToString();
        }

        private static void RenderBindMethod(IndentedStringBuilder indent, BoundType model)
        {
            var typeName = model.TypeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
            indent.AppendLine($"public static async System.Threading.Tasks.ValueTask<{typeName}> BindAsync(Microsoft.AspNetCore.Http.HttpContext ctx)");
            indent.AppendLine("{");
            indent.Indent();
            // Determine if we need to read the form from the request. We only read
            // it once even if multiple properties come from the form.
            var formNeeded = model.Properties.Any(p => p.Source == SourceKind.Form);
            if (formNeeded)
            {
                indent.AppendLine("var form = await ctx.Request.ReadFormAsync().ConfigureAwait(false);");
            }
            indent.AppendLine("var query = ctx.Request.Query;");
            indent.AppendLine("var route = ctx.Request.RouteValues;");
            indent.AppendLine();
            // Declare and bind each property
            foreach (var prop in model.Properties)
            {
                GeneratePropertyBinding(indent, prop, formNeeded);
            }
            indent.AppendLine();
            // Instantiate the DTO either via a constructor or default constructor
            if (model.Constructor != null && model.Constructor.Parameters.Length > 0)
            {
                // Constructor initialisation
                indent.Append($"var result = new {GetTypeName(model.TypeSymbol)}(");
                indent.AppendLine();
                indent.Indent();
                for (int i = 0; i < model.Constructor.Parameters.Length; i++)
                {
                    var mapping = model.ConstructorMapping[i];
                    var propName = mapping.property.Name;
                    if (i < model.Constructor.Parameters.Length - 1)
                    {
                        indent.AppendLine($"{propName},");
                    }
                    else
                    {
                        indent.AppendLine($"{propName}");
                    }
                }
                indent.Unindent();
                indent.AppendLine(");");
                // Assign remaining properties not mapped through the constructor
                var remaining = model.Properties.Where(p => !model.ConstructorMapping.Any(m => m.property == p));
                foreach (var prop in remaining)
                {
                    indent.AppendLine($"result.{prop.Name} = {prop.Name};");
                }
                indent.AppendLine("return new(result);");
            }
            else
            {
                // Parameterless constructor path
                indent.AppendLine($"var result = new {GetTypeName(model.TypeSymbol)}();");
                foreach (var prop in model.Properties)
                {
                    indent.AppendLine($"result.{prop.Name} = {prop.Name};");
                }
                indent.AppendLine("return new(result);");
            }
            indent.Unindent();
            indent.AppendLine("}");
        }

        private static void GeneratePropertyBinding(IndentedStringBuilder indent, BoundProperty prop, bool formNeeded)
        {
            var varName = prop.Name;
            var typeName = GetTypeName(prop.Symbol.Type);
            // Declare the variable and assign an initial default
            indent.Append($"{typeName} {varName} = ");
            if (prop.IsCollection)
            {
                indent.AppendLine($"new {typeName}();");
            }
            else if (prop.IsNullable || prop.Symbol.Type.IsReferenceType)
            {
                indent.AppendLine("default!;");
            }
            else
            {
                indent.AppendLine("default;");
            }
            // Begin binding scope
            indent.AppendLine("{");
            indent.Indent();
            // Handle collections
            if (prop.IsCollection)
            {
                GenerateCollectionBinding(indent, prop, formNeeded);
            }
            else if (prop.IsComplex)
            {
                // Delegate to helper for nested complex type
                var helperName = $"Bind_{GetSanitisedTypeName(prop.Symbol.Type)}";
                var formArg = formNeeded ? "form" : "null";
                indent.AppendLine($"{varName} = {helperName}(ctx, \"{prop.KeyName}.\", {formArg}, query, route);");
            }
            else
            {
                // Bind simple value (primitive, string, enum, guid)
                // Determine the value string based on the source
                var valueVar = $"value_{varName}";
                switch (prop.Source)
                {
                    case SourceKind.Query:
                        indent.AppendLine($"var {valueVar} = query[\"{prop.KeyName}\"].Count > 0 ? query[\"{prop.KeyName}\"].ToString() : null;");
                        break;
                    case SourceKind.Route:
                        indent.AppendLine($"var {valueVar} = route.TryGetValue(\"{prop.KeyName}\", out var tmp_{varName}) ? tmp_{varName}?.ToString() : null;");
                        break;
                    case SourceKind.Form:
                    default:
                        if (formNeeded)
                        {
                            indent.AppendLine($"var {valueVar} = form[\"{prop.KeyName}\"].Count > 0 ? form[\"{prop.KeyName}\"].ToString() : null;");
                        }
                        else
                        {
                            indent.AppendLine($"var {valueVar} = ctx.Request.Form[\"{prop.KeyName}\"].Count > 0 ? ctx.Request.Form[\"{prop.KeyName}\"].ToString() : null;");
                        }
                        break;
                }
                // Assign based on type
                if (prop.IsString)
                {
                    indent.AppendLine($"{varName} = {valueVar};");
                }
                else if (prop.IsGuid)
                {
                    indent.AppendLine($"if ({valueVar} != null && System.Guid.TryParse({valueVar}, out var guidResult)) {varName} = guidResult;");
                }
                else if (prop.IsEnum)
                {
                    var enumTypeName = GetTypeName(prop.Symbol.Type);
                    indent.AppendLine($"if ({valueVar} != null && System.Enum.TryParse<{enumTypeName}>({valueVar}, true, out var enumResult)) {varName} = enumResult;");
                }
                else if (prop.IsPrimitive)
                {
                    var tryParse = GetTryParseMethod(prop.Symbol.Type);
                    indent.AppendLine($"if ({valueVar} != null && {tryParse}({valueVar}, out var parsedValue)) {varName} = parsedValue;");
                }
                else
                {
                    indent.AppendLine($"// Unsupported simple type {typeName}, value ignored");
                }
            }
            indent.Unindent();
            indent.AppendLine("}");
        }

